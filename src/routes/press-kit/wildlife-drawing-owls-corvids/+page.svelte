      <h3 class="text-4xl">Appleton Drawing Club Hosts Rare Wildlife Art Event to Benefit Local Bird Rescue</h3>

      <p class="font-bold">Local artists get chance to draw live rescued owls and ravens while raising funds for wildlife conservation</p>

      <p><strong>APPLETON, WI</strong> – October 29, 2025 – Appleton Drawing Club will host a special Halloween wildlife drawing session featuring live owls, ravens, and an albino crow from the Raptor Education Group, Inc. (REGI). The event, running from 7-9 PM at The Photo Opp, offers participants a rare opportunity to draw rescued birds up close while supporting local wildlife conservation efforts.</p>

      <p>"A true interaction with a wild being is incredibly rare," said Kevin McGillivray, organizer of Appleton Drawing Club. "The chance to do so for a few hours and carefully pay attention with deep curiosity is even rarer. Wildlife drawing expands our sense of what's possible and what we overlook in our day-to-day routines."</p>

      <p>The REGI education team plans to bring several "Avian Ambassadors" – permanent resident birds who assist with public education events. Featured animals include Aldo the Barred Owl, Jasper and Lucy the Eastern Screech-Owls, Rosie the albino American Crow, and potentially Eclipse the Raven.</p>

      <p>These birds are permanent residents at REGI who are unable to be released into the wild, making them ideal ambassadors for wildlife education. While learning the stories behind these birds, participants will experience the unique challenge of drawing live subjects.</p>

      <p>"You have to practice going with the flow," McGillivray explained. "The animals might move around, although they do tend to settle in and hold surprisingly still when the room is full of focused artists. In the end we're not really drawing exactly what the subject looks like, but rather what it feels like to be there together in the moment."</p>

      <p>The event builds on the success of a February wildlife drawing session that became the drawing club's most popular event to date. That session featured three hawks under the care of local licensed falconers.</p>

      <p>All proceeds from the Halloween event will be donated to REGI, which provides care and rehabilitation for 800-1,000 injured or orphaned native birds annually. Founded in 1990, REGI holds permits from both Wisconsin and the U.S. Fish and Wildlife Service to possess threatened and endangered indigenous species for rehabilitation and educational purposes.</p>

      <p>"REGI provides a safe haven for injured or orphaned native birds until they are ready to be released back into the wild," McGillivray noted. "They also advocate for birds who can't speak for their own needs for habitat and safety through education and research."</p>

      <p>The collaboration came about through Brandi Grahl, a Photo Opp board member and licensed falconer who helped organize the February wildlife session. Photo Opp has supported Appleton Drawing Club since its founding in April 2024.</p>

      <p>"Photo Opp is a truly transformative center of creative incubation in Appleton," McGillivray said. "The goals of photography and life drawing are largely the same—to expand the range of our vision and curiosity."</p>

      <p>McGillivray founded Appleton Drawing Club with the belief that life drawing creates "a world more curious and kind." The club provides regular access to high-quality life drawing sessions and welcomes artists of all experience levels.</p>

      <p>"You don't need to have years of experience or a grand artistic vision to make art," McGillivray emphasized. "You just need a little time with a pencil, some paper, and a fascinating subject."</p>

      <p><em>Wildlife Drawing: Owls & Corvids</em> takes place October 29, 7-9 PM at The Photo Opp in Appleton. Registration and additional information are available through the Appleton Drawing Club website, <a href="/events/2025-10-29-halloween-wildlife-drawing">https://appletondrawingclub.com/events/2025-10-29-halloween-wildlife-drawing</a>.</p>
